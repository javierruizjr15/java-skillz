package primitiveDataDefiniteLoops;

public class Window19 {
    public static void main(String[] args) {
        for (int i = 1; i <= 2; i++) {
            System.out.println("+===+===+");
            for (int j = 1; j <= 3; j++) {
                System.out.println("|   |   |");
            }
        }
        System.out.println("+===+===+");
    }
}